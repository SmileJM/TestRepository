package com.mycompany.myapplication.dto;


public class Software {
    private int sno;
    private int sphoto;
    private String sname;
    private int sstar;
    private String sdesc;

    public int getSno() {
        return sno;
    }

    public void setSno(int sno) {
        this.sno = sno;
    }

    public int getSphoto() {
        return sphoto;
    }

    public void setSphoto(int sphoto) {
        this.sphoto = sphoto;
    }

    public String getSname() {
        return sname;
    }

    public void setSname(String sname) {
        this.sname = sname;
    }

    public int getSstar() {
        return sstar;
    }

    public void setSstar(int sstar) {
        this.sstar = sstar;
    }

    public String getSdesc() {
        return sdesc;
    }

    public void setSdesc(String sdesc) {
        this.sdesc = sdesc;
    }
}
